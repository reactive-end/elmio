export function estiloPorDefecto(tipo: TipoSeccion): EstiloSeccion {
  const base: EstiloSeccion = {
    paddingSuperior: 80,
    paddingDerecho: 24,
    paddingInferior: 80,
    paddingIzquierdo: 24,
    margenSuperior: 0,
    margenInferior: 0,
    colorFondo: 'transparente',
    gradienteFondo: '',
    imagenFondo: '',
    opacidadOverlay: 0,
    anchoBorde: 0,
    colorBorde: '#e5e7eb',
    radioBorde: 0,
    estiloBorde: 'solido',
    tituloTamano: 36,
    tituloPeso: 700,
    tituloColor: '#111827',
    tituloAlineacion: 'centro',
    subtituloTamano: 18,
    subtituloColor: '#6b7280',
    cuerpoTamano: 16,
    cuerpoColor: '#374151',
    cuerpoAlineacion: 'centro',
    anchoMaximo: 1200,
    espacioEntreElementos: 24,
    botonColorFondo: '#0f4ece',
    botonColorTexto: '#ffffff',
    botonRedondez: 12,
    altoImagenProducto: 200,
    carruselAuto: true,
    carruselVelocidad: 5,
  }

  switch (tipo) {
    case 'principal':
      return {
        ...base,
        gradienteFondo: 'linear-gradient(135deg, oklch(0.45 0.25 264) 0%, oklch(0.3 0.2 220) 100%)',
        tituloColor: '#ffffff',
        subtituloColor: 'rgba(255,255,255,0.7)',
        paddingSuperior: 120,
        paddingInferior: 120,
        tituloTamano: 48,
      }
    case 'banner':
      return {
        ...base,
        colorFondo: '#0f4ece',
        tituloColor: '#ffffff',
        subtituloColor: 'rgba(255,255,255,0.8)',
        cuerpoColor: 'rgba(255,255,255,0.7)',
        paddingSuperior: 48,
        paddingInferior: 48,
        tituloTamano: 28,
      }
    case 'caracteristicas':
      return { ...base, colorFondo: '#f9fafb', tituloTamano: 32, espacioEntreElementos: 32 }
    case 'productos':
      return { ...base, tituloTamano: 32, espacioEntreElementos: 24, altoImagenProducto: 220 }
    case 'pie':
      return {
        ...base,
        colorFondo: '#111827',
        tituloColor: '#ffffff',
        subtituloColor: '#9ca3af',
        cuerpoColor: '#6b7280',
        paddingSuperior: 48,
        paddingInferior: 48,
        tituloTamano: 16,
        tituloPeso: 600,
        espacioEntreElementos: 16,
      }
    case 'texto':
      return {
        ...base,
        paddingSuperior: 48,
        paddingInferior: 48,
        tituloTamano: 28,
        tituloPeso: 600,
        tituloAlineacion: 'izquierda',
        cuerpoAlineacion: 'izquierda',
        anchoMaximo: 800,
      }
    case 'doble-banner':
      return {
        ...base,
        paddingSuperior: 48,
        paddingInferior: 48,
        espacioEntreElementos: 24,
        tituloTamano: 24,
      }
    case 'franja':
      return {
        ...base,
        colorFondo: '#f3f4f6',
        paddingSuperior: 24,
        paddingInferior: 24,
        tituloTamano: 20,
        tituloPeso: 600,
        carruselAuto: true,
        carruselVelocidad: 4,
      }
    case 'pilares':
      return {
        ...base,
        colorFondo: '#f9fafb',
        tituloTamano: 32,
        espacioEntreElementos: 24,
        tituloAlineacion: 'centro',
      }
    case 'cabecera':
      return {
        ...base,
        colorFondo: '#ffffff',
        paddingSuperior: 12,
        paddingInferior: 12,
        tituloTamano: 18,
        tituloPeso: 600,
      }
    default:
      return base
  }
}

